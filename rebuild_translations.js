async function rebuildTranslations() {
  console.log('Creating backup of current files...');
  
  // Backup current files
  const currentTime = Date.now();
  const backupDir = `shared/translations/backups/${new Date().toISOString().split('T')[0]}`;
  fs.mkdirSync(path.join(__dirname, backupDir), { recursive: true });
  
  const filesToBackup = ['en.ts', 'sq.ts'];
  for (const file of filesToBackup) {
    const srcPath = path.join(__dirname, 'shared/translations', file);
    const backupPath = path.join(__dirname, backupDir, `${file}.backup-${currentTime}`);
    if (fs.existsSync(srcPath)) {
      fs.copyFileSync(srcPath, backupPath);
      console.log(`ğŸ“ Backed up ${file}`);
    }
  }
  
  // Create clean English translations
  const enPath = path.join(__dirname, 'shared/translations/en.ts');
  fs.writeFileSync(enPath, createCleanEnglishTranslations());
  console.log('âœ… Created clean English translations');
  
  // Create clean Albanian translations
  const sqPath = path.join(__dirname, 'shared/translations/sq.ts');
  fs.writeFileSync(sqPath, createCleanAlbanianTranslations());
  console.log('âœ… Created clean Albanian translations');
  
  console.log('\nğŸ‰ Translation rebuild completed!');
  console.log('ğŸ“Š File sizes:');
  console.log(`   English: ${(fs.statSync(enPath).size / 1024).toFixed(2)} KB`);
  console.log(`   Albanian: ${(fs.statSync(sqPath).size / 1024).toFixed(2)} KB`);
}

